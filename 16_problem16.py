# Create two sets, set1 and set2. Perform union and intersection operations and print the results.

# Step 1: Create set1 and set2

set1 = {1, 2, 3, 4, 5}
set2 = {3, 4, 5, 6, 7, 8}

# Step 2: Perform union operation

union_set = set1.union(set2)
print("Union of set1 and set2:", union_set)

# Step 3: Perform intersection operation

interset_set = set1.intersection(set2)
print("Intersection of set1 and set2:", interset_set)